package 산술연산자;

public class Ex04증감연산자 {

	public static void main(String[] args) {
		int num =4;
		//증감연산자 --> 앞에적으면 바로적용 뒤에적으면 후에 적용이며 -- 또한 같다.
		//이는 ++ 또는 -- 사용함에 따라서 1이라는 숫자의 증감이 존재한다.
		System.out.println(++num);//바로적용
		System.out.println(num);
		System.out.println(num++);//다음번에적용
		System.out.println(num);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		

	}

}
